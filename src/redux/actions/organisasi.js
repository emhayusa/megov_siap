import {
  CREATE_ORGANISASI_SUCCESS,
  CREATE_ORGANISASI_FAIL,
  RETRIEVE_ORGANISASI_SUCCESS,
  RETRIEVE_ORGANISASI_FAIL,
  UPDATE_ORGANISASI_SUCCESS,
  UPDATE_ORGANISASI_FAIL,
  DELETE_ORGANISASI_SUCCESS,
  DELETE_ORGANISASI_FAIL,
  SET_MESSAGE,
} from "./types";

import Service from "src/services/organisasi.service";
import EventBus from "src/utils/EventBus";

export const retrieve = () => async (dispatch) => {
  try {
    const res = await Service.getAll();

    dispatch({
      type: RETRIEVE_ORGANISASI_SUCCESS,
      payload: res.data,
    });
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    console.log(err);
  }
};

export const create = (nama_organisasi) => async (dispatch) => {
  try {
    const res = await Service.create({
      nama_organisasi
    });

    dispatch({
      type: CREATE_ORGANISASI_SUCCESS,
      payload: res.data,
    });

    return Promise.resolve(res.data);
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    return Promise.reject(err);
  }
};

export const update = (uuid, data) => async (dispatch) => {
  try {
    const res = await Service.update(uuid, data);

    dispatch({
      type: UPDATE_ORGANISASI_SUCCESS,
      payload: data,
    });

    return Promise.resolve(res.data);
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    return Promise.reject(err);
  }
};

export const remove = (uuid) => async (dispatch) => {
  try {
    await Service.remove(uuid);

    dispatch({
      type: DELETE_ORGANISASI_SUCCESS,
      payload: { uuid },
    });
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    console.log(err);
  }
};
