import {
  CREATE_PANGKAT_SUCCESS,
  CREATE_PANGKAT_FAIL,
  RETRIEVE_PANGKAT_SUCCESS,
  RETRIEVE_PANGKAT_FAIL,
  UPDATE_PANGKAT_SUCCESS,
  UPDATE_PANGKAT_FAIL,
  DELETE_PANGKAT_SUCCESS,
  DELETE_PANGKAT_FAIL,
  SET_MESSAGE,
} from "./types";

import Service from "src/services/pangkat.service";
import EventBus from "src/utils/EventBus";

export const retrieve = () => async (dispatch) => {
  try {
    const res = await Service.getAll();

    dispatch({
      type: RETRIEVE_PANGKAT_SUCCESS,
      payload: res.data,
    });
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    console.log(err);
  }
};

export const create = (nama_pangkat) => async (dispatch) => {
  try {
    const res = await Service.create({
      nama_pangkat
    });

    dispatch({
      type: CREATE_PANGKAT_SUCCESS,
      payload: res.data,
    });

    return Promise.resolve(res.data);
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    return Promise.reject(err);
  }
};

export const update = (uuid, data) => async (dispatch) => {
  try {
    const res = await Service.update(uuid, data);

    dispatch({
      type: UPDATE_PANGKAT_SUCCESS,
      payload: data,
    });

    return Promise.resolve(res.data);
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    return Promise.reject(err);
  }
};

export const remove = (uuid) => async (dispatch) => {
  try {
    await Service.remove(uuid);

    dispatch({
      type: DELETE_PANGKAT_SUCCESS,
      payload: { uuid },
    });
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    console.log(err);
  }
};
